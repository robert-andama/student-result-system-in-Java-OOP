    private void jb_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_searchActionPerformed

        if (!jt_id.getText().equals("")) {
            enable();
            String regnoo = jt_id.getText();

            try {
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/java_proj", "root", "");

                Statement st = con.createStatement();
                String q = "SELECT * FROM student WHERE regno = " + regnoo;
                ResultSet rs = st.executeQuery(q);
                
                System.out.println(rs);
                
                jt_fname.setText("");
                jt_lname.setText("");
                jt_address.setText("");
                jt_program.setText("");
                jt_age.setText("");
                jt_phone.setText("");
                jt_email.setText("");
                while (rs.next()) {
                    found = true;
                    jt_id.setText((rs.getString("regno")));
                    jt_fname.setText(rs.getString("fname"));
                    jt_lname.setText(rs.getString("lname"));
                    jc_gender.setSelectedItem((Object) rs.getString("gender"));
                    jt_address.setText(rs.getString("address"));
                    jt_program.setText(rs.getString("programme"));
                    jt_age.setText(rs.getString("age"));
                    jt_phone.setText(rs.getString("phoneNo"));
                    jt_email.setText(rs.getString("email"));
                    //JOptionPane.showMessageDialog(this, "PRESENT . .  . . . .SIR", "Message", JOptionPane.INFORMATION_MESSAGE);
                }
                found = false;
            } catch (SQLException sqlex) {
                System.out.print(sqlex.toString());
                System.out.print("HEEEEEEE");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please enter registration no.", "Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jb_searchActionPerformed

