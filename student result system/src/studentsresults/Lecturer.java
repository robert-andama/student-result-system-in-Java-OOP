    private void allCourseUnits() {
        // sql statment
        connect.setSql("SELECT DISTINCT`course_code` FROM `enroll` ORDER BY `enroll`.`course_code` ASC");
        //get result
        ResultSet result_enroll = connect.getResultSet();
        try {
            // set data on to fileds
            while (result_enroll.next()) {
                String code = result_enroll.getString("course_code");
                jc_units.addItem(code);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Lecturer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

