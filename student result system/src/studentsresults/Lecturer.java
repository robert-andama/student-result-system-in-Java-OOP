    private void jb_resultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_resultActionPerformed
        String cmbitem = (String) jc_units.getSelectedItem();
        // sql statment
        connect.setSql("SELECT *FROM enroll where course_code='" + cmbitem + "'");
        //get result
        ResultSet result = connect.getResultSet();
        // set data on to table
        jt_table.setModel(DbUtils.resultSetToTableModel(result));
        // sql statment
        connect.setSql("SELECT * FROM enroll WHERE course_code = '" + cmbitem + "'");
        //get result
        ResultSet result_enroll = connect.getResultSet();
        try {
            // set data on to fileds
            while (result_enroll.next()) {
                jf_id.setText((result_enroll.getString("Regno")));
                jf_unit.setText(result_enroll.getString("courseUnit"));
                jt_marks.setText(result_enroll.getString("marks"));
                jt_grade.setText(result_enroll.getString("grade"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Lecturer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jb_resultActionPerformed

