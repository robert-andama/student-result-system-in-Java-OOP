    private void jm_printActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jm_printActionPerformed
        if (jt_results.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill in the registration number field");
        } else {

            // sql statment
            connect.setSql("Select * from enroll where regno='" + jt_results.getText() + "';");
            //get result
            ResultSet result_print = connect.getResultSet();
            try {
                // set data on to fileds
                while (result_print.next()) {
                    lname = result_print.getString(1);
                    fname = result_print.getString(2);
                    mname = result_print.getString(3);
                    course = result_print.getString(4);
                    grad = result_print.getString(5);
                }
                PrintResults printResults = new PrintResults(lname, fname, mname, course, grad);
                printResults.setVisible(true);
                printResults.setLocationRelativeTo(null);
            } catch (SQLException ex) {

            }
        }
    }//GEN-LAST:event_jm_printActionPerformed

